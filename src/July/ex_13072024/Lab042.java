package July.ex_13072024;

public class Lab042 {
    public static void main(String[] args) {
        // Relational Operators -> boolean
        //    // >, < >=,<=, == , != ( ! = ) - true or false.
        int age_mamitha = 34;
        int age_hari = 34;
        boolean result = age_hari > age_mamitha;
        boolean result1 = age_hari < age_mamitha;
        boolean result2 = age_hari >= age_mamitha; // OR gate
        System.out.println(result);
        System.out.println(result1);
        System.out.println(result2);

    }
}
